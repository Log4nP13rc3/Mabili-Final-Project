                <h3>Introduction</h3>
                <p>Time Period: Mid-20th century (1950s-1960s).</p>
                <p>Geographical Origin: Originated in the United Kingdom and the United States.</p>
                <p>Historical Context: Pop Art emerged in the post-World War II era as a reaction against Abstract Expressionism and a reflection of the rise of mass media and consumer culture. The movement coincided with the economic boom and cultural changes of the 1950s and 1960s.</p>

                <h3>Characteristics</h3>
                <p>Main Features: Bold colors, graphic imagery, and the use of popular culture references, such as advertising, comic strips, and celebrity icons. Pop Art often blurs the line between high art and popular culture.</p>
                <p>Philosophy & Ideals: The movement seeks to challenge traditional notions of art and culture, often embracing irony, satire, and a celebration of the mundane. Pop Art reflects the consumerist and media-saturated society of the time.</p>
                <p>Innovations: The incorporation of mass-produced imagery and commercial techniques, such as screen printing, into fine art. Pop Art also influenced later movements, such as Conceptual Art and Postmodernism.</p>

                <h3>Key Artists</h3>
                <ul>
                    <li>Andy Warhol: Known for his iconic images of celebrities, consumer products, and mass media, Warhol explored themes of fame, commodification, and the banal.</li>
                    <li>Roy Lichtenstein: Celebrated for his comic strip-inspired works, Lichtenstein used bold colors, graphic lines, and Ben-Day dots to create satirical and ironic commentary on art and culture.</li>
                    <li>Claes Oldenburg: A sculptor known for his large-scale, often playful depictions of everyday objects, such as food and household items.</li>
                </ul>

