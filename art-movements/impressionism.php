                <h1 class="page-heading">Impressionism</h1>
                
                <h3>Introduction:</h3>
                <p>Time Period: Late 19th century.</p>
                <p>Geographical Origin: Originated in France, particularly in Paris.</p>
                <p>Historical Context: Impressionism emerged as a reaction against the formal, academic art of the time. The movement sought to capture the fleeting effects of light and color in everyday scenes, often painted en plein air (outdoors).</p>
                
                <h3>Characteristics:</h3>
                <p>Main Features: Use of loose brushwork, bright colors, and a focus on light and atmosphere. Impressionist artists often painted scenes of modern life, such as urban landscapes, leisure activities, and nature.</p>
                <p>Philosophy & Ideals: Impressionism emphasized the artist’s perception of a moment, capturing the transient effects of light and movement rather than creating highly detailed, finished works.</p>
                <p>Innovations: The use of broken color, visible brushstrokes, and an emphasis on the play of natural light.</p>
                
                <h3>Key Artists:</h3>
                <ul>
                    <li>Claude Monet: Known for his depictions of light and color, particularly in his series paintings of water lilies and haystacks.</li>
                    <li>Pierre-Auguste Renoir: Celebrated for his vibrant scenes of Parisian life and his mastery of color.</li>
                    <li>Edgar Degas: Known for his dynamic compositions and focus on movement, particularly in his depictions of dancers.</li>
                </ul>

