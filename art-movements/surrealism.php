                <h3>Cultural Impact:</h3>
                <p>Reception: Surrealism was widely recognized for its innovative approach and impact on various art forms. It influenced later movements and remains a significant part of the art historical canon.</p>
                <p>Societal Influence: The movement’s exploration of the unconscious mind and the irrational influenced not only art but also literature, film, and psychology.</p>
                <p>Legacy: Surrealism’s techniques and themes have continued to resonate in contemporary art and culture, influencing a range of artistic and creative practices.</p>

                <h3>Conclusion:</h3>
                <p>Summary: Surrealism explored the unconscious mind and embraced the irrational, creating art that challenged perceptions of reality and had a lasting impact on modern culture.</p>

