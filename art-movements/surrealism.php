                <h1 class="page-heading">Surrealism</h1>
                
                <h3>Introduction:</h3>
                <p>Time Period: Early to mid-20th century (1920s-1950s).</p>
                <p>Geographical Origin: Originated in France.</p>
                <p>Historical Context: Surrealism developed out of Dada and was influenced by the psychological theories of Sigmund Freud. The movement sought to explore the unconscious mind and create art that transcended reality, often through dreamlike and fantastical imagery.</p>
                
                <h3>Characteristics:</h3>
                <p>Main Features: Use of dreamlike, fantastical imagery, often combining the real and the imaginary in strange and unexpected ways. Surrealism emphasized the exploration of the unconscious mind and sought to liberate thought from rational constraints.</p>
                <p>Philosophy & Ideals: Surrealism aimed to tap into the unconscious mind and explore the mysteries of the human psyche. The movement embraced chance, automatism, and the irrational, seeking to reveal deeper truths through art.</p>
                <p>Innovations: Techniques such as automatism (spontaneous creation without conscious control) and the juxtaposition of unrelated objects and imagery. Surrealism also influenced film, literature, and theater.</p>
                
                <h3>Key Artists:</h3>
                <ul>
                    <li>Salvador Dalí: Known for his meticulous, dreamlike paintings that explore themes of time, memory, and the subconscious.</li>
                    <li>René Magritte: Celebrated for his witty and thought-provoking images that challenge perceptions of reality.</li>
                    <li>Max Ernst: A pioneer of Surrealist techniques, such as frottage and collage, known for his fantastical and often unsettling imagery.</li>
                </ul>

