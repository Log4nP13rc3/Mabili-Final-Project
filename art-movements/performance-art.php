                <h3>Introduction:</h3>
                <p>Time Period: Mid-20th century to present (1960s-present).</p>
                <p>Geographical Origin: Originated in the United States and Europe.</p>
                <p>Historical Context: Performance Art emerged as a movement that prioritized live action and the body as the medium for artistic expression. It developed alongside other avant-garde movements, challenging traditional art forms and engaging with social and political themes.</p>
                
                <h3>Characteristics:</h3>
                <p>Main Features: Live, often ephemeral, performances that may involve the artist’s body, audience interaction, and various media. Performance Art can be scripted or improvisational and often emphasizes the process and the moment of the performance.</p>
                <p>Philosophy & Ideals: The movement explores themes of identity, power, and the relationship between artist and audience. Performance Art emphasizes the experience of the live event and often seeks to provoke thought or challenge conventions.</p>
                <p>Innovations: The use of the body as a primary medium, the incorporation of audience participation, and the blending of art with daily life and political activism. Performance Art also intersects with other disciplines such as theater, dance, and multimedia art.</p>
                
