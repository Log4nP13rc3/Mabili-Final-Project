                <h1 class="page-heading">Conceptual Art</h1>
                
                <h3>Introduction:</h3>
                <p>Time Period: Mid-20th century (1960s-1970s).</p>
                <p>Geographical Origin: Originated in the United States and Europe.</p>
                <p>Historical Context: Conceptual Art emerged as a reaction against traditional notions of art as a visual object. The movement focused on the idea or concept behind the artwork, often prioritizing intellectual engagement over aesthetic experience.</p>
                
                <h3>Characteristics:</h3>
                <p>Main Features: Emphasis on ideas and concepts over traditional aesthetic concerns. Conceptual Art often uses language, text, and documentation to convey meaning. The movement challenges the role of the artist and the definition of art itself.</p>
                <p>Philosophy & Ideals: Conceptual Art seeks to prioritize the idea or concept behind the artwork, often questioning the value of the art object. The movement embraces a wide range of media and methods, from text and photography to performance and installation.</p>
                <p>Innovations: The use of non-traditional materials and processes, such as text and documentation. Conceptual Art also challenged the role of the artist and the viewer, emphasizing intellectual engagement over visual experience.</p>
                
                <h3>Key Artists:</h3>
                <ul>
                    <li>Sol LeWitt: Known for his wall drawings and sculptures that emphasize the idea behind the work over its physical execution.</li>
                    <li>Joseph Kosuth: A pioneer of Conceptual Art, known for his use of language and text to explore the relationship between art and meaning.</li>
                    <li>Yoko Ono: An artist and performer whose works often engage with themes of participation, instruction, and the intersection of art and life.</li>
                </ul>

