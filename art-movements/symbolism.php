                <h1 class="page-heading">Symbolism</h1>
                
                <h3>Introduction:</h3>
                <p>Time Period: Late 19th century.</p>
                <p>Geographical Origin: Originated in France and Belgium, with influence across Europe.</p>
                <p>Historical Context: Symbolism emerged as a reaction against the materialism and realism of the time. It sought to express the mystical, spiritual, and emotional aspects of human experience through symbolic imagery and themes.</p>
                
                <h3>Characteristics:</h3>
                <p>Main Features: Use of symbolic imagery to convey abstract ideas and emotions. Symbolist art often featured dreamlike, mysterious scenes and figures, with an emphasis on mood and atmosphere.</p>
                <p>Philosophy & Ideals: Symbolism emphasized the inner world of the artist, focusing on the spiritual and metaphysical rather than the physical world. It often explored themes of love, death, dreams, and the unconscious.</p>
                <p>Innovations: The use of allegory, mythological and religious symbolism, and a focus on the evocative power of color and form.</p>
                
                <h3>Key Artists:</h3>
                <ul>
                    <li>Gustave Moreau: Known for his mythological and allegorical paintings filled with rich symbolism.</li>
                    <li>Odilon Redon: Celebrated for his dreamlike and fantastical imagery, often exploring the subconscious.</li>
                    <li>Edvard Munch: His work, particularly The Scream, captures the existential angst and emotional intensity characteristic of Symbolism.</li>
                </ul>

