                <h3>Introduction:</h3>
                <p>Time Period: Early 20th century (1904-1908).</p>
                <p>Geographical Origin: Originated in France.</p>
                <p>Historical Context: Fauvism emerged as one of the first avant-garde movements of the 20th century. It was a reaction against the academic art of the 19th century and was characterized by a bold, expressive use of color.</p>
                
                <h3>Characteristics:</h3>
                <p>Main Features: Use of bright, non-naturalistic colors, bold brushstrokes, and simplified forms. Fauvist artists sought to express emotion through color rather than represent reality accurately.</p>
                <p>Philosophy & Ideals: Fauvism emphasized spontaneity, vibrant color, and individual expression. The movement prioritized emotional response over realistic representation, often using exaggerated colors to convey mood.</p>
                <p>Innovations: The liberation of color from its descriptive role, allowing it to serve as a means of personal expression.</p>
                
                <h3>Key Artists:</h3>
                <ul>
                    <li>Henri Matisse: The leading figure of Fauvism, known for his vibrant, expressive use of color.</li>
                    <li>André Derain: Celebrated for his bold color choices and dynamic compositions.</li>
                    <li>Maurice de Vlaminck: Known for his intense, emotional landscapes that exemplify the Fauvist approach.</li>
                </ul>

