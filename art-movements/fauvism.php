                <p>Reception: Fauvism was initially met with criticism for its radical use of color, but it later gained recognition as a major influence on modern art.</p>
                <p>Societal Influence: The movement challenged traditional notions of color and representation, opening new possibilities for artistic expression in the 20th century.</p>
                <p>Legacy: Fauvism’s emphasis on color and emotion influenced later movements, including Expressionism and Abstract art.</p>

