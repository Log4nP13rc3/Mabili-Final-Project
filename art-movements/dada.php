                <h3>Cultural Impact:</h3>
                <p>Reception: Dada was controversial and often misunderstood, but it gained recognition for its radical ideas and influence on later avant-garde movements.</p>
                <p>Societal Influence: The movement’s emphasis on absurdity and anti-art challenged societal norms and inspired later developments in art, literature, and performance.</p>
                <p>Legacy: Dada’s influence can be seen in later movements, such as Surrealism, Conceptual Art, and Postmodernism. Its challenge to traditional values continues to resonate in contemporary art.</p>

