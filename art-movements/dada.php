                <h3>Introduction:</h3>
                <p>Time Period: Early 20th century (1916-1924).</p>
                <p>Geographical Origin: Originated in Zurich, Switzerland.</p>
                <p>Historical Context: Dada emerged in the aftermath of World War I, as a reaction against the perceived senselessness and destruction of the war. The movement rejected traditional artistic values and sought to challenge established norms through absurdity and satire.</p>
                
                <h3>Characteristics:</h3>
                <p>Main Features: Use of absurdity, nonsense, and anti-art concepts. Dada artists often employed collage, readymades, and performance to challenge traditional notions of art and aesthetics.</p>
                <p>Philosophy & Ideals: Dada was rooted in a rejection of logic, reason, and the established order. The movement sought to disrupt conventional thinking and provoke thought through irrationality and absurdity.</p>
                <p>Innovations: The introduction of readymades—ordinary objects presented as art—and the use of chance and spontaneity in the creative process. Dada also pioneered performance art and multimedia experiments.</p>
                
                <h3>Key Artists:</h3>
                <ul>
                    <li>Marcel Duchamp: Known for his readymades, such as Fountain, which challenged the definition of art.</li>
                    <li>Hannah Höch: A pioneer of photomontage, whose works often critiqued societal norms and gender roles.</li>
                    <li>Tristan Tzara: A key figure in Dada’s literary and performance activities, known for his manifestos and poetry.</li>
                </ul>

