                <h3>Introduction:</h3>
                <p>Time Period: Early 20th century (1907-1914).</p>
                <p>Geographical Origin: Originated in France.</p>
                <p>Historical Context: Cubism emerged as a radical departure from traditional perspectives in art, driven by a desire to represent multiple viewpoints simultaneously. It was heavily influenced by the geometric simplification of forms seen in African and Iberian art.</p>
                
                <h3>Characteristics:</h3>
                <p>Main Features: Fragmentation of objects into geometric shapes, often depicted from multiple angles simultaneously. Cubism rejected traditional perspective and sought to represent the subject in a more abstract, deconstructed manner.</p>
                <p>Philosophy & Ideals: Cubism emphasized the breakdown of objects into their basic forms, challenging the notion of art as a mirror of reality. It sought to represent the complexity of perception and reality through abstraction.</p>
                <p>Innovations: Introduction of collage techniques, the use of monochromatic color palettes, and the development of two distinct phases: Analytical Cubism (more fragmented and abstract) and Synthetic Cubism (more focused on collage and construction).</p>
                
                <h3>Key Artists:</h3>
                <ul>
                    <li>Pablo Picasso: A co-founder of Cubism, known for his radical deconstruction of form and exploration of multiple perspectives.</li>
                    <li>Georges Braque: Picasso’s collaborator and fellow pioneer of Cubism, known for his abstracted still lifes and use of collage.</li>
                    <li>Juan Gris: A key figure in Synthetic Cubism, known for his more colorful and structured compositions.</li>
                </ul>

