                <h1 class="page-heading">Post-Impressionism</h1>
                
                <h3>Introduction:</h3>
                <p>Time Period: Late 19th to early 20th century.</p>
                <p>Geographical Origin: Originated in France, following the Impressionist movement.</p>
                <p>Historical Context: Post-Impressionism developed as a reaction to the limitations of Impressionism. While Post-Impressionist artists retained the vibrant colors and interest in light from Impressionism, they sought to explore more symbolic and emotional content, leading to a diverse range of styles.</p>
                
                <h3>Characteristics:</h3>
                <p>Main Features: A focus on structure, form, and symbolic content, often with more defined shapes and lines than Impressionism. Post-Impressionists used bold colors, expressive brushstrokes, and experimented with new techniques.</p>
                <p>Philosophy & Ideals: Post-Impressionism sought to convey deeper meanings and emotions in art, moving beyond the transient effects of light and color to explore the artist's inner world and the symbolic potential of color and form.</p>
                <p>Innovations: Introduction of pointillism, expressive use of color, and a move towards abstraction.</p>
                
                <h3>Key Artists:</h3>
                <ul>
                    <li>Vincent van Gogh: Known for his expressive use of color and emotion.</li>
                    <li>Paul Cézanne: Celebrated for his exploration of form and structure, laying the groundwork for Cubism.</li>
                    <li>Georges Seurat: A pioneer of pointillism, using tiny dots of color to create luminous effects.</li>
                </ul>

