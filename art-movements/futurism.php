                <h1 class="page-heading">Futurism</h1>
                
                <h3>Introduction:</h3>
                <p>Time Period: Early 20th century (1909-1944).</p>
                <p>Geographical Origin: Originated in Italy.</p>
                <p>Historical Context: Futurism emerged in the context of rapid technological advancements and industrialization. The movement celebrated modernity, speed, and innovation, and was closely tied to political and social changes in pre-World War I Europe.</p>
                
                <h3>Characteristics:</h3>
                <p>Main Features: Depiction of motion, speed, and dynamism, often through fragmented forms and repeated lines. Futurism glorified the energy and technology of the modern world, rejecting traditional artistic conventions.</p>
                <p>Philosophy & Ideals: The movement embraced progress, technology, and the future, often with a militant and revolutionary fervor. Futurists sought to break free from the past and create a new, modern aesthetic that reflected the pace of contemporary life.</p>
                <p>Innovations: The use of bold, dynamic compositions that conveyed movement and energy. Futurism also experimented with abstract forms and influenced later developments in film and photography.</p>
                
                <h3>Key Artists:</h3>
                <ul>
                    <li>Umberto Boccioni: Known for his dynamic sculptures and paintings that captured the movement and energy of modern life.</li>
                    <li>Giacomo Balla: Celebrated for his depictions of speed and light, often through abstract forms and vibrant colors.</li>
                    <li>Carlo Carrà: A key figure in the movement, known for his bold compositions and exploration of modern themes.</li>
                </ul>

