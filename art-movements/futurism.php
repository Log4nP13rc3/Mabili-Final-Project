                <h3>Cultural Impact:</h3>
                <p>Reception: Futurism was both celebrated and criticized for its radical ideas and association with political movements. Its bold aesthetics and themes of modernity influenced many artists and designers of the 20th century.</p>
                <p>Societal Influence: The movement had a significant impact on various disciplines, including architecture, film, and design, and contributed to the broader cultural embrace of modernity and technology.</p>
                <p>Legacy: Futurism’s emphasis on movement and abstraction influenced later movements, including Constructivism, Dada, and Abstract art.</p>

                <h3>Conclusion:</h3>
                <p>Summary: Futurism celebrated the energy and dynamism of the modern world, rejecting tradition and embracing progress, and left a lasting impact on 20th-century art and culture.</p>

