                <h3>Introduction:</h3>
                <p>Time Period: Late 20th century to present (1960s-present).</p>
                <p>Geographical Origin: Originated in the United States and Europe.</p>
                <p>Historical Context: Installation Art emerged as a movement that challenged traditional notions of art as an object, emphasizing the viewer’s experience of a space or environment. The movement coincided with broader cultural shifts towards experiential and participatory art.</p>
                
                <h3>Characteristics:</h3>
                <p>Main Features: Large-scale, immersive works that transform a space or environment. Installation Art often involves a variety of media, including sculpture, video, sound, and performance. The work is often site-specific, designed to engage with the particular characteristics of the location.</p>
                <p>Philosophy & Ideals: The movement emphasizes the viewer’s physical and sensory experience of the artwork, often encouraging interaction or participation. Installation Art explores themes of space, context, and the relationship between the viewer and the work.</p>
                <p>Innovations: The integration of diverse media and materials, as well as the use of unconventional spaces such as galleries, museums, and public spaces. Installation Art also challenges traditional art forms and encourages new ways of engaging with art.</p>
                
