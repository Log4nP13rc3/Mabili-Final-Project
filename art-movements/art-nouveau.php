                <h3>Cultural Impact:</h3>
                <p>Reception: Art Nouveau was embraced by the elite and became popular in architecture, design, and advertising, though it eventually gave way to more modern styles.</p>
                <p>Societal Influence: The movement influenced a wide range of disciplines, from graphic design to architecture, and sought to make art more accessible to the public.</p>
                <p>Legacy: Art Nouveau’s emphasis on organic forms and craftsmanship influenced later movements, including Modernism and Art Deco.</p>

                <h3>Conclusion:</h3>
                <p>Summary: Art Nouveau sought to bring beauty into everyday life, blending art and design with nature’s forms, and leaving a lasting impact on the decorative arts and architecture.</p>

