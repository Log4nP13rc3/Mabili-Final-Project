                <h1 class="page-heading">Art Nouveau</h1>
                
                <h3>Introduction:</h3>
                <p>Time Period: Late 19th to early 20th century.</p>
                <p>Geographical Origin: Originated in Europe, particularly in France and Belgium.</p>
                <p>Historical Context: Art Nouveau emerged as a reaction against the academic art of the 19th century and the industrial revolution. It sought to bring art into everyday life, blurring the lines between fine and applied arts.</p>
                
                <h3>Characteristics:</h3>
                <p>Main Features: Use of flowing, organic lines, intricate patterns, and motifs inspired by nature, such as plants, flowers, and animals. Art Nouveau emphasized decorative arts and craftsmanship.</p>
                <p>Philosophy & Ideals: The movement aimed to create a unified aesthetic, where art and design could enhance everyday life. It celebrated the beauty of natural forms and sought to harmonize art with nature.</p>
                <p>Innovations: The integration of art into architecture, furniture, and decorative objects. Art Nouveau artists often used new materials and techniques, such as glass and ironwork, to create their designs.</p>
                
                <h3>Key Artists:</h3>
                <ul>
                    <li>Gustav Klimt: Known for his decorative, symbolic paintings with intricate patterns.</li>
                    <li>Alphonse Mucha: Celebrated for his iconic posters and designs, which epitomize the flowing lines and floral motifs of Art Nouveau.</li>
                    <li>Hector Guimard: A leading architect whose work, including the famous Paris Métro entrances, exemplifies the movement’s integration of art and architecture.</li>
                </ul>

