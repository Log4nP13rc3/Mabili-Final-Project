                <h1 class="page-heading">Neoclassicism</h1>
                
                <h3>Introduction:</h3>
                <p>Neoclassicism spanned from the mid-18th to the early 19th century, originating in Europe, particularly in France and Britain. It emerged as a reaction to the excesses of Rococo and was influenced by archaeological discoveries of ancient Greek and Roman sites, inspiring a revival of classical ideals in art and architecture.</p>
                
                <h3>Characteristics:</h3>
                <p>Neoclassicism emphasized classical forms, simplicity, and symmetry. Art often depicted heroic subjects and historical themes, focusing on moral virtue. The movement sought to return to the "purity" of classical art, promoting reason, discipline, and civic responsibility. Innovations included the use of clear lines, balanced compositions, and restrained color palettes.</p>
                
                <h3>Key Artists:</h3>
                <ul>
                    <li>Jacques-Louis David: Known for his historical and political paintings that exemplified Neoclassical ideals.</li>
                    <li>Jean-Auguste-Dominique Ingres: Celebrated for his precise, linear style and portrayal of classical themes.</li>
                    <li>Antonio Canova: A leading sculptor whose works reflected the grace and balance of classical antiquity.</li>
                </ul>

