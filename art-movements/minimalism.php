                <h1 class="page-heading">Minimalism</h1>
                
                <h3>Introduction:</h3>
                <p>Time Period: Mid-20th century (1960s-1970s).</p>
                <p>Geographical Origin: Originated in the United States.</p>
                <p>Historical Context: Minimalism emerged as a reaction against the emotional intensity of Abstract Expressionism and the commercialism of Pop Art. The movement focused on simplicity, form, and the elimination of excess.</p>
                
                <h3>Characteristics:</h3>
                <p>Main Features: Use of simple, geometric forms and a limited color palette. Minimalism emphasized the physical properties of materials and often rejected representational imagery. The focus was on purity of form and the viewer’s direct experience of the artwork.</p>
                <p>Philosophy & Ideals: Minimalism sought to strip art down to its essentials, emphasizing clarity, precision, and the removal of personal expression. The movement aimed to create a more objective and impersonal approach to art.</p>
                <p>Innovations: The use of industrial materials and processes, as well as the rejection of traditional artistic techniques. Minimalism also influenced architecture, design, and music.</p>
                
                <h3>Key Artists:</h3>
                <ul>
                    <li>Donald Judd: Known for his geometric, industrial sculptures that emphasize form and space.</li>
                    <li>Dan Flavin: Celebrated for his use of fluorescent light to create minimalist installations that explore color and space.</li>
                    <li>Agnes Martin: A painter known for her serene, grid-based works that explore subtle variations in line and tone.</li>
                </ul>

