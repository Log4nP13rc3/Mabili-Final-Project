                <h1 class="page-heading">Abstract Expressionism</h1>
                
                <h3>Introduction:</h3>
                <p>Time Period: Mid-20th century (1940s-1950s).</p>
                <p>Geographical Origin: Originated in the United States, particularly New York City.</p>
                <p>Historical Context: Abstract Expressionism emerged in the aftermath of World War II, as artists sought new ways to express emotion and explore abstraction. The movement marked a shift in the art world’s focus from Europe to the United States.</p>
                
                <h3>Characteristics:</h3>
                <p>Main Features: Large-scale, abstract works that emphasize spontaneity, emotion, and the physical act of painting. Abstract Expressionism is often divided into two approaches: Action Painting (emphasizing gesture and movement) and Color Field Painting (focusing on large areas of color and subtle variations in tone).</p>
                <p>Philosophy & Ideals: The movement emphasized individual expression and the exploration of the unconscious. Artists sought to convey emotion and inner experience through abstraction, often rejecting traditional forms and techniques.</p>
                <p>Innovations: The use of unconventional materials and techniques, such as dripping and pouring paint, as well as the emphasis on the physicality of the creative process. Abstract Expressionism also marked a shift towards larger canvases and bolder, more dramatic compositions.</p>
                
                <h3>Key Artists:</h3>
                <ul>
                    <li>Jackson Pollock: Known for his drip paintings, which epitomize the spontaneity and energy of Action Painting.</li>
                    <li>Mark Rothko: A pioneer of Color Field Painting, known for his large, luminous canvases that evoke deep emotional responses.</li>
                    <li>Willem de Kooning: Celebrated for his dynamic, gestural paintings that combine abstraction and figuration.</li>
                </ul>

