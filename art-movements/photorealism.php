                <h1 class="page-heading">Photorealism</h1>

                <h3>Introduction</h3>
                <p>Time Period: Late 20th century (1960s-1970s).</p>
                <p>Geographical Origin: Originated in the United States.</p>
                <p>Historical Context: Photorealism emerged as a reaction against Abstract Expressionism and Minimalism, focusing on the meticulous reproduction of photographs in painting and sculpture. The movement coincided with the rise of photography as a dominant medium in art and culture.</p>

                <h3>Characteristics</h3>
                <p>Main Features: Highly detailed, realistic depictions of everyday scenes, often based on photographs. Photorealism emphasizes precision and technical skill, with a focus on surface detail and texture.</p>
                <p>Philosophy & Ideals: The movement seeks to challenge the boundaries between photography and painting, often exploring themes of reality, representation, and perception. Photorealists strive for accuracy and fidelity to the photographic source, sometimes questioning the nature of reality itself.</p>
                <p>Innovations: The use of photographic techniques and tools, such as projectors and grids, to create highly detailed and realistic paintings. Photorealism also influenced later movements that explored the relationship between photography and other media.</p>

                <h3>Key Artists</h3>
                <ul>
                    <li>Chuck Close: Known for his large-scale portraits that meticulously reproduce photographic images, often using grids and pixel-like patterns.</li>
                    <li>Richard Estes: Celebrated for his urban scenes and reflective surfaces, which capture the complexity and detail of modern life.</li>
                    <li>Audrey Flack: A painter and sculptor known for her vibrant, hyper-realistic still lifes that often include symbolic objects and personal references.</li>
                </ul>

