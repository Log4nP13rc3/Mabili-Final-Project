                <h1 class="page-heading">Realism</h1>
                
                <h3>Introduction:</h3>
                <p>Time Period: Mid-19th century.</p>
                <p>Geographical Origin: Originated in France and spread across Europe and the United States.</p>
                <p>Historical Context: Realism emerged as a response to the idealized and emotionalized subjects of Romanticism. It sought to depict the everyday lives of ordinary people with honesty and accuracy, often addressing social issues and the realities of modern life.</p>
                
                <h3>Characteristics:</h3>
                <p>Main Features: Focus on everyday scenes and ordinary people, often depicting laborers, peasants, and urban life. Realist art emphasizes accuracy, detail, and a neutral, objective viewpoint.</p>
                <p>Philosophy & Ideals: Realism sought to represent life as it truly was, without idealization or romanticization. It often conveyed a critical perspective on social issues and the human condition.</p>
                <p>Innovations: The use of naturalistic techniques, attention to detail, and the depiction of contemporary life as worthy of artistic representation.</p>
                
                <h3>Key Artists:</h3>
                <ul>
                    <li>Gustave Courbet: Known for his depictions of rural life and laborers, often challenging traditional notions of beauty.</li>
                    <li>Jean-François Millet: Celebrated for his sympathetic portrayals of peasants and rural life.</li>
                    <li>Édouard Manet: His work bridged Realism and Impressionism, often depicting modern urban life with a fresh, unidealized perspective.</li>
                </ul>

