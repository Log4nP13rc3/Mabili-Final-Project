                <p>Time Period: Late 20th century (1980s-present).</p>
                <p>Geographical Origin: Originated in urban environments, particularly in the United States and Europe.</p>
                <p>Historical Context: Street Art emerged from the graffiti culture of the 1970s and 1980s, evolving into a broader movement that encompasses various forms of public art, including murals, stencils, and installations. The movement reflects the social, political, and cultural changes of the late 20th and early 21st centuries.</p>

                <h3>Characteristics</h3>
                <p>Main Features: Street Art is often characterized by its public, urban setting and its use of unconventional materials and techniques. The movement embraces a wide range of styles and forms, from graffiti and stencils to murals and installations.</p>
                <p>Philosophy & Ideals: The movement seeks to challenge traditional notions of art, often embracing themes of activism, social justice, and community engagement. Street Art is often seen as a form of resistance and a way to reclaim public spaces.</p>
                <p>Innovations: The use of public spaces as a canvas for art, often without permission or outside the traditional gallery system. Street Art has also embraced new technologies and media, such as digital art and augmented reality.</p>

                <h3>Key Artists</h3>
                <ul>
                    <li>Banksy: Known for his politically charged stencils and street interventions, Banksy is one of the most famous and influential street artists in the world.</li>
                    <li>Shepard Fairey: A graphic designer and street artist known for his iconic "Obey" campaign and the "Hope" poster for Barack Obama’s 2008 presidential campaign.</li>
                    <li>JR: A French artist known for his large-scale photographic installations in public spaces, often addressing social and political issues.</li>
