                <h3>Introduction</h3>
                <p>Time Period: Mid-20th century (1960s).</p>
                <p>Geographical Origin: Originated in the United States and Europe.</p>
                <p>Historical Context: Op Art, short for Optical Art, emerged in the 1960s as a movement that explored visual perception and illusion. The movement was influenced by developments in psychology, science, and technology.</p>

                <h3>Characteristics</h3>
                <p>Main Features: Use of geometric shapes, patterns, and colors to create optical illusions. Op Art often employs techniques such as moiré patterns, contrasting colors, and repetitive forms to create the illusion of movement or depth.</p>
                <p>Philosophy & Ideals: Op Art seeks to engage the viewer’s visual perception and challenge the way we see and interpret images. The movement emphasizes the physical and psychological effects of color and form.</p>
                <p>Innovations: The use of optical illusions and geometric abstraction to create dynamic, often disorienting visual effects. Op Art also influenced fashion, design, and advertising.</p>

                <h3>Key Artists</h3>
                <ul>
                    <li>Victor Vasarely: Known as the father of Op Art, celebrated for his use of geometric shapes and optical illusions to create dynamic, abstract compositions.</li>
                    <li>Bridget Riley: A leading figure in the movement, known for her black-and-white and color-based works that explore visual perception and illusion.</li>
                    <li>Julio Le Parc: An Argentine artist known for his kinetic and optical works that engage with light, movement, and perception.</li>
                </ul>

