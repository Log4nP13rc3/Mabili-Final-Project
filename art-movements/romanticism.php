                <h3>Introduction:</h3>
                <p>Romanticism flourished from the late 18th to the mid-19th century in Europe, with significant developments in Britain, Germany, and France. It emerged as a reaction to the rationalism of the Enlightenment and the industrialization of society, emphasizing emotion, individualism, and the sublime power of nature.</p>
                
                <h3>Characteristics:</h3>
                <p>Romanticism is characterized by its emphasis on emotion, imagination, and the beauty of nature. Art often depicted dramatic scenes, exotic landscapes, and heroic individuals. The movement celebrated the individual's emotional experience, the supernatural, and the untamed aspects of nature, using vivid colors, dramatic contrasts, and dynamic compositions to convey emotion and movement.</p>
                
                <h3>Key Artists:</h3>
                <ul>
                    <li>Eugène Delacroix: Known for his vibrant color palette and dramatic scenes.</li>
                    <li>J.M.W. Turner: Celebrated for his atmospheric landscapes and exploration of light and color.</li>
                    <li>Caspar David Friedrich: His work focused on the sublime power of nature and the individual’s place within it.</li>
                </ul>

