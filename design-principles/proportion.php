                <h1 class="page-heading">Proportion</h1>
                
                <h3>Definition:</h3>
                <p>Proportion in art refers to the relative size and scale of elements within a composition. It involves the relationship between the sizes of different parts of the artwork and how they contribute to the overall balance and harmony. Proper proportion can enhance realism and create a sense of order.</p>
                
                <h3>Keywords:</h3>
                <ul>
                    <li>Scale</li>
                    <li>Size relationships</li>
                    <li>Ratio</li>
                    <li>Balance</li>
                    <li>Hierarchy</li>
                    <li>Realism</li>
                    <li>Order</li>
                    <li>Relative size</li>
                    <li>Visual impact</li>
                </ul>

                <h3>Analysis:</h3>
                <p>When analyzing proportion in an artwork, consider the following:</p>
                <ul>
                    <li><strong>Size Relationships:</strong> Observe how the sizes of different elements relate to each other. Are they proportional or exaggerated?</li>
                    <li><strong>Balance and Harmony:</strong> Identify how proportion contributes to the overall balance and harmony of the composition. Does it create a sense of equilibrium?</li>
                    <li><strong>Realism and Order:</strong> Reflect on how proportion affects realism and order within the artwork. Does it enhance or disrupt the sense of realism?</li>
                    <li><strong>Visual Impact:</strong> Consider the visual impact of the proportion. Does it draw attention to certain areas or create a specific effect?</li>
                </ul>

