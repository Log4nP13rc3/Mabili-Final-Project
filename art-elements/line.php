                <h1 class="page-heading">Line</h1>
                
                <h3>Definition:</h3>
                <p>Line is an element of art defined as a point moving in space. It can vary in width, direction, and length. Lines can be horizontal, vertical, or diagonal, straight or curved, thick or thin. They can be used to define shapes, create textures, suggest movement, or convey emotions.</p>
                
                <h3>Keywords:</h3>
                <ul>
                    <li>Horizontal line</li>
                    <li>Vertical line</li>
                    <li>Diagonal line</li>
                    <li>Curved line</li>
                    <li>Contour line</li>
                    <li>Implied line</li>
                    <li>Gesture line</li>
                    <li>Line weight</li>
                    <li>Direction</li>
                    <li>Movement</li>
                </ul>

                <h3>Analysis:</h3>
                <p>When analyzing line in an artwork, consider the following:</p>
                <ul>
                    <li><strong>Type of Lines:</strong> Identify the types of lines used—are they straight, curved, or a combination? How do they contribute to the overall composition?</li>
                    <li><strong>Line Direction:</strong> Observe the direction of the lines. Do horizontal lines create a sense of calm, or do diagonal lines add a dynamic feel?</li>
                    <li><strong>Line Weight:</strong> Consider the thickness or thinness of the lines. How does the variation in line weight affect the emphasis or focus within the artwork?</li>
                    <li><strong>Contour and Gesture:</strong> Look at how lines define the edges of forms (contour lines) or suggest movement and energy (gesture lines).</li>
                    <li><strong>Implied Lines:</strong> Reflect on whether the artist uses implied lines (lines that are suggested rather than physically drawn) to guide the viewer’s eye through the composition.</li>
                </ul>

