                <h1 class="page-heading">Value</h1>
                
                <h3>Definition:</h3>
                <p>Value refers to the lightness or darkness of a color in an artwork. It is an essential element of art that creates contrast, highlights, and shadows, contributing to the perception of depth and volume. Value is often used to define form, create a focal point, or set the mood of the artwork.</p>
                
                <h3>Keywords:</h3>
                <ul>
                    <li>Light value</li>
                    <li>Dark value</li>
                    <li>High contrast</li>
                    <li>Low contrast</li>
                    <li>Shading</li>
                    <li>Highlights</li>
                    <li>Midtones</li>
                    <li>Chiaroscuro</li>
                    <li>Gradient</li>
                    <li>Tonal range</li>
                </ul>

                <h3>Analysis:</h3>
                <p>When analyzing value in an artwork, consider the following:</p>
                <ul>
                    <li><strong>Contrast:</strong> Observe the contrast between light and dark values. Is there a strong contrast that creates drama, or a subtle gradient that adds softness?</li>
                    <li><strong>Light Source:</strong> Consider the direction and source of light in the artwork. How does the use of light and shadow define the forms and contribute to the sense of depth?</li>
                    <li><strong>Chiaroscuro:</strong> Reflect on the use of chiaroscuro (the contrast of light and dark) to create a three-dimensional effect. How does this technique enhance the realism or drama of the artwork?</li>
                    <li><strong>Mood:</strong> Consider how the value range sets the mood. Are dark values used to create a somber or mysterious atmosphere, or are light values used to convey brightness and clarity?</li>
                </ul>

