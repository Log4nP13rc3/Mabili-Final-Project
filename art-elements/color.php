                <h1 class="page-heading">Color</h1>
                
                <h3>Definition:</h3>
                <p>Color is an element of art that is produced when light, striking an object, is reflected back to the eye. It has three main characteristics: hue (the name of the color, such as red, blue, etc.), value (how light or dark it is), and intensity (how bright or dull it is). Colors can be classified into primary, secondary, and tertiary, and they interact in various ways to create contrast, harmony, and emphasis in an artwork.</p>
                
                <h3>Keywords:</h3>
                <ul>
                    <li>Hue</li>
                    <li>Saturation</li>
                    <li>Tone</li>
                    <li>Warm colors (e.g., red, orange, yellow)</li>
                    <li>Cool colors (e.g., blue, green, purple)</li>
                    <li>Complementary colors</li>
                    <li>Analogous colors</li>
                    <li>Monochromatic</li>
                    <li>Color harmony</li>
                    <li>Color contrast</li>
                </ul>

                <h3>Analysis:</h3>
                <p>When analyzing color in an artwork, consider the following:</p>
                <ul>
                    <li><strong>Hue:</strong> Identify the dominant hues used and consider how they interact. Are they warm or cool? How do they contribute to the overall mood?</li>
                    <li><strong>Value:</strong> Observe the lightness or darkness of the colors. How does the artist use light and dark colors to create contrast or highlight certain areas?</li>
                    <li><strong>Intensity:</strong> Look at the brightness or dullness of the colors. Are the colors vibrant or subdued? How does this affect the feeling of the artwork?</li>
                    <li><strong>Color Relationships:</strong> Consider how colors are placed next to each other. Are complementary colors used to create contrast, or are analogous colors used for harmony?</li>
                    <li><strong>Emotional Impact:</strong> Reflect on the emotional response the colors evoke. Does the color scheme create a sense of calm, tension, excitement, or melancholy?</li>
                </ul>

