                <h1 class="page-heading">Shape</h1>
                
                <h3>Definition:</h3>
                <p>Shape is an element of art that is two-dimensional, flat, or limited to height and width. Shapes can be geometric (like squares and circles) or organic (free-form or natural shapes). They can define areas in an artwork and contribute to its overall composition.</p>
                
                <h3>Keywords:</h3>
                <ul>
                    <li>Geometric shapes</li>
                    <li>Organic shapes</li>
                    <li>Positive shape</li>
                    <li>Negative shape</li>
                    <li>Silhouette</li>
                    <li>Form</li>
                    <li>Figure-ground relationship</li>
                    <li>Abstract shapes</li>
                    <li>Symmetry</li>
                    <li>Asymmetry</li>
                </ul>

                <h3>Analysis:</h3>
                <p>When analyzing shape in an artwork, consider the following:</p>
                <ul>
                    <li><strong>Geometric vs. Organic:</strong> Identify whether the shapes are geometric or organic. Geometric shapes can create a sense of order, while organic shapes often suggest spontaneity and fluidity.</li>
                    <li><strong>Positive and Negative Shapes:</strong> Observe the relationship between the positive shapes (the shapes themselves) and the negative shapes (the spaces around and between them). How do these relationships affect the composition?</li>
                    <li><strong>Figure-Ground Relationship:</strong> Consider how the shapes interact with the background. Do they stand out clearly, or do they blend into the background?</li>
                    <li><strong>Symmetry and Asymmetry:</strong> Reflect on whether the shapes are symmetrical, creating balance and stability, or asymmetrical, adding dynamism and interest to the composition.</li>
                </ul>

