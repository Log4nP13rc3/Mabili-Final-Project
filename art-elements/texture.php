                <h1 class="page-heading">Texture</h1>
                
                <h3>Definition:</h3>
                <p>Texture refers to the surface quality of an object, how it feels, or how it looks like it would feel. In art, texture can be actual (tactile, felt with touch) or implied (visual, suggested by the way the artist represents surfaces). Texture adds depth and interest to an artwork, creating a more engaging and realistic portrayal.</p>
                
                <h3>Keywords:</h3>
                <ul>
                    <li>Actual texture</li>
                    <li>Implied texture</li>
                    <li>Rough</li>
                    <li>Smooth</li>
                    <li>Soft</li>
                    <li>Hard</li>
                    <li>Matte</li>
                    <li>Glossy</li>
                    <li>Tactile</li>
                    <li>Visual texture</li>
                </ul>

                <h3>Analysis:</h3>
                <p>When analyzing texture in an artwork, consider the following:</p>
                <ul>
                    <li><strong>Actual vs. Implied Texture:</strong> Determine whether the texture is actual (can be felt) or implied (suggested visually). How does this influence the viewer’s experience of the artwork?</li>
                    <li><strong>Surface Quality:</strong> Observe the surface qualities of different areas in the artwork. Are they rough, smooth, soft, or hard? How does the texture contribute to the overall impression of the piece?</li>
                    <li><strong>Contrast:</strong> Reflect on how different textures are used in contrast with one another. Does the juxtaposition of smooth and rough surfaces create visual interest or highlight specific areas?</li>
                    <li><strong>Tactile Sensation:</strong> Consider the sensory experience that the textures evoke. Does the implied texture make you imagine the sensation of touch?</li>
                </ul>

