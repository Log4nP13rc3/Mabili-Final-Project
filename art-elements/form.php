                <h1 class="page-heading">Form</h1>
                
                <h3>Definition:</h3>
                <p>Form refers to a three-dimensional object or the illusion of three dimensions in art. Forms have height, width, and depth, and can be geometric (e.g., cubes, spheres, cylinders) or organic (irregular and natural shapes). In two-dimensional artwork, form is often implied through the use of light and shadow, perspective, or shading techniques.</p>
                
                <h3>Keywords:</h3>
                <ul>
                    <li>Geometric forms (e.g., cube, sphere, cylinder)</li>
                    <li>Organic forms</li>
                    <li>Volume</li>
                    <li>Mass</li>
                    <li>Perspective</li>
                    <li>Light and shadow</li>
                    <li>Depth</li>
                    <li>Modeling</li>
                </ul>

                <h3>Analysis:</h3>
                <p>When analyzing form in an artwork, consider the following:</p>
                <ul>
                    <li><strong>Type of Form:</strong> Determine whether the forms are geometric or organic. Geometric forms tend to be more structured and formal, while organic forms are more fluid and natural.</li>
                    <li><strong>Volume and Mass:</strong> Consider the size and bulk of the forms. Do they appear solid and heavy, or light and delicate?</li>
                    <li><strong>Light and Shadow:</strong> Observe how light interacts with the forms. Are there strong contrasts between light and shadow, or is the shading subtle?</li>
                    <li><strong>Depth:</strong> Consider how the artist creates a sense of depth. Is perspective used to create the illusion of three-dimensional space, or is the depth suggested through the arrangement of forms?</li>
                    <li><strong>Interaction:</strong> Reflect on how the forms interact within the composition. Are they overlapping, interlocking, or isolated?</li>
                </ul>

