                <h1 class="page-heading">Space</h1>
                
                <h3>Definition:</h3>
                <p>Space refers to the area around, between, and within objects in an artwork. Space can be positive (filled with objects) or negative (empty areas). Artists use space to create the illusion of depth, perspective, and distance, or to emphasize relationships between different elements in the composition.</p>
                
                <h3>Keywords:</h3>
                <ul>
                    <li>Positive space</li>
                    <li>Negative space</li>
                    <li>Foreground</li>
                    <li>Middle ground</li>
                    <li>Background</li>
                    <li>Overlapping</li>
                    <li>Perspective</li>
                    <li>Depth</li>
                    <li>Scale</li>
                    <li>Proportion</li>
                </ul>

                <h3>Analysis:</h3>
                <p>When analyzing space in an artwork, consider the following:</p>
                <ul>
                    <li><strong>Positive and Negative Space:</strong> Identify how the artist uses positive and negative space. Is there a balance between the two, or is one more dominant?</li>
                    <li><strong>Perspective and Depth:</strong> Observe how the artist creates the illusion of depth. Is linear perspective used, or is depth suggested through overlapping and placement of objects?</li>
                    <li><strong>Scale and Proportion:</strong> Consider the scale and proportion of objects within the space. How does the relative size of objects affect the perception of space?</li>
                    <li><strong>Foreground, Middle ground, Background:</strong> Reflect on how the artist organizes the composition into foreground, middle ground, and background. How does this structure guide the viewer’s eye through the artwork?</li>
                </ul>

