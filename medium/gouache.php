                <h1 class="page-heading">Gouache</h1>
                
                <h3>Introduction:</h3>
                <p>Gouache is an opaque watercolor medium that is known for its vibrant colors and matte finish. It is similar to watercolor but has a higher pigment concentration and is mixed with a white pigment to increase opacity.</p>
                
                <h3>History:</h3>
                <p>Gouache has been used since ancient times, but it became particularly popular in the Renaissance and later periods. Artists appreciated its versatility and the ability to create rich, solid colors.</p>

                <h3>Characteristics:</h3>
                <ul>
                    <li><strong>Opacity:</strong> Gouache is opaque, allowing for solid color application without the transparency of watercolors.</li>
                    <li><strong>Matte Finish:</strong> Dries to a matte finish, which can be desirable for certain artistic effects.</li>
                    <li><strong>Vibrant Colors:</strong> High pigment content results in vivid, saturated colors.</li>
                    <li><strong>Reworkability:</strong> Can be reworked even after drying, unlike some other mediums.</li>
                </ul>

                <h3>Techniques:</h3>
                <ul>
                    <li><strong>Layering:</strong> Building up layers of gouache to achieve depth and complexity.</li>
                    <li><strong>Wet-on-Dry:</strong> Applying gouache onto a dry surface to create crisp edges and detailed areas.</li>
                    <li><strong>Dry Brush:</strong> Using a dry brush technique to achieve a textured, painterly effect.</li>
                    <li><strong>Glazing:</strong> Applying thin layers of gouache over dry layers to modify color and add depth.</li>
                </ul>

                <h3>Care and Preservation:</h3>
                <p>Gouache paintings should be kept away from direct sunlight to prevent fading. Frame under glass or store in a protective sleeve to avoid damage. Keep in a cool, dry place.</p>

