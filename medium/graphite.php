                <h1 class="page-heading">Graphite</h1>
                
                <h3>Introduction:</h3>
                <p>Graphite is a popular drawing medium known for its smooth application and range of tonal values. It is a form of carbon used in pencils and is appreciated for its versatility and ease of use in creating detailed and expressive drawings.</p>
                
                <h3>History:</h3>
                <p>Graphite was first discovered in the 16th century, and its use in pencils became widespread in the 19th century. It has been a fundamental medium for artists throughout history, from Renaissance sketches to contemporary works.</p>

                <h3>Characteristics:</h3>
                <ul>
                    <li><strong>Range of Values:</strong> Provides a broad spectrum of tones from light grays to deep blacks.</li>
                    <li><strong>Blendability:</strong> Easily blended with tools or fingers for smooth transitions.</li>
                    <li><strong>Detail and Precision:</strong> Ideal for fine lines and intricate details.</li>
                    <li><strong>Ease of Use:</strong> Pencils are easy to control and adjust for various effects.</li>
                </ul>

                <h3>Techniques:</h3>
                <ul>
                    <li><strong>Hatching:</strong> Creating tonal values with parallel lines.</li>
                    <li><strong>Cross-Hatching:</strong> Building up tone and texture with intersecting lines.</li>
                    <li><strong>Stippling:</strong> Using dots to create texture and shading.</li>
                    <li><strong>Blending:</strong> Smoothing out graphite with blending stumps or fingers for gradient effects.</li>
                </ul>

                <h3>Care and Preservation:</h3>
                <p>Graphite drawings should be protected from smudging by using a fixative spray. Store in a cool, dry place and avoid touching the surface to maintain the integrity of the drawing.</p>

