                <h1 class="page-heading">Charcoal</h1>
                
                <h3>Introduction:</h3>
                <p>Charcoal is a drawing medium made from burnt wood, providing rich, deep blacks and a range of tonal values. It is favored for its versatility and the ability to create both fine details and broad, expressive strokes.</p>
                
                <h3>History:</h3>
                <p>Charcoal has been used as a drawing medium since prehistoric times. It was a common tool in the sketchbooks of Old Masters and remains popular among contemporary artists for its simplicity and expressive potential.</p>

                <h3>Characteristics:</h3>
                <ul>
                    <li><strong>Rich Black:</strong> Produces deep, intense blacks that can be used to create dramatic contrasts.</li>
                    <li><strong>Blendability:</strong> Easily blended with fingers or tools for smooth transitions and shading.</li>
                    <li><strong>Versatility:</strong> Can be used for detailed drawings or large, expressive marks.</li>
                    <li><strong>Erased Easily:</strong> Can be partially or fully erased to create highlights and textures.</li>
                </ul>

                <h3>Techniques:</h3>
                <ul>
                    <li><strong>Stippling:</strong> Creating texture and depth by placing dots of charcoal in varied densities.</li>
                    <li><strong>Cross-Hatching:</strong> Using intersecting lines to build up tone and texture.</li>
                    <li><strong>Blending:</strong> Using blending stumps or fingers to smooth out and transition tones.</li>
                    <li><strong>Lift-Off:</strong> Using an eraser to lift off charcoal to create highlights and details.</li>
                </ul>

                <h3>Care and Preservation:</h3>
                <p>Charcoal drawings should be fixed with a spray fixative to prevent smudging. Store in a cool, dry place and handle with care to avoid smudging or damage.</p>

