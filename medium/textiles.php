                <h1 class="page-heading">Textiles</h1>
                
                <h3>Introduction:</h3>
                <p>Textile art involves creating visual art through the manipulation of fibers and fabrics. Techniques include weaving, embroidery, dyeing, and fabric painting, resulting in a diverse range of artistic expressions.</p>
                
                <h3>History:</h3>
                <p>Textile art has been practiced for thousands of years, with early examples from ancient civilizations like Egypt and China. The development of textile techniques has evolved through various cultures and periods, influencing fashion and decorative arts.</p>

                <h3>Characteristics:</h3>
                <ul>
                    <li><strong>Material Diversity:</strong> Uses a wide range of fibers, fabrics, and techniques.</li>
                    <li><strong>Texture and Pattern:</strong> Offers rich textures and patterns that can be both functional and decorative.</li>
                    <li><strong>Color Application:</strong> Techniques like dyeing and painting provide vibrant colors and designs.</li>
                    <li><strong>Versatility:</strong> Can be used for both traditional and contemporary artistic expressions.</li>
                </ul>

                <h3>Techniques:</h3>
                <ul>
                    <li><strong>Weaving:</strong> Interlacing fibers to create fabric, used in both traditional and modern textile art.</li>
                    <li><strong>Embroidery:</strong> Decorating fabric with needle and thread to create intricate designs.</li>
                    <li><strong>Dyeing:</strong> Applying color to fabric through various methods, including batik and tie-dye.</li>
                    <li><strong>Fabric Painting:</strong> Using paints to create designs directly on fabric.</li>
                </ul>

                <h3>Care and Preservation:</h3>
                <p>Textile artworks should be handled with care to avoid damage. Store in a cool, dry place, and avoid direct sunlight. Clean according to the fabric's care instructions, and consider professional conservation for valuable pieces.</p>

