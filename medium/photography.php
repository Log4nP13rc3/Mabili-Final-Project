                <h1 class="page-heading">Photography</h1>
                
                <h3>Introduction:</h3>
                <p>Photography is the art of capturing images through the use of light-sensitive materials or digital sensors. It encompasses various styles and techniques, from traditional film photography to modern digital practices.</p>
                
                <h3>History:</h3>
                <p>Photography was invented in the early 19th century, with pioneers such as Joseph Nicéphore Niépce and Louis Daguerre. The evolution of photography includes advancements from daguerreotypes to digital cameras.</p>

                <h3>Characteristics:</h3>
                <ul>
                    <li><strong>Detail:</strong> Captures fine details and textures with high precision.</li>
                    <li><strong>Color Accuracy:</strong> Provides accurate color representation, depending on the camera and settings.</li>
                    <li><strong>Versatility:</strong> Can be used for various purposes, including artistic expression, documentation, and commercial work.</li>
                    <li><strong>Instant Feedback:</strong> Digital photography allows for immediate review and adjustment of images.</li>
                </ul>

                <h3>Techniques:</h3>
                <ul>
                    <li><strong>Composition:</strong> Arranging elements within the frame to create a visually appealing image.</li>
                    <li><strong>Exposure:</strong> Adjusting the camera settings to control the amount of light that hits the sensor.</li>
                    <li><strong>Focus:</strong> Using the camera's focus to highlight specific areas and create depth.</li>
                    <li><strong>Post-Processing:</strong> Editing images digitally to enhance or alter their appearance.</li>
                </ul>

                <h3>Care and Preservation:</h3>
                <p>Store photographic prints in a dry, dark environment to prevent fading. Digital images should be backed up regularly and stored on reliable media to avoid data loss.</p>

