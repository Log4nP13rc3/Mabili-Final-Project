                <h1 class="page-heading">Polymer Clay</h1>
                
                <h3>Introduction:</h3>
                <p>Polymer clay is a versatile, synthetic modeling material that hardens when baked. Its pliability and range of colors make it a popular medium for creating detailed sculptures, jewelry, and other decorative objects.</p>
                
                <h3>History:</h3>
                <p>Polymer clay was first developed in the 1930s but became widely used by artists in the 1960s. It has since grown in popularity, with artists like Maureen Carlson and Christi Friesen elevating the medium to new levels of artistic expression.</p>

                <h3>Characteristics:</h3>
                <ul>
                    <li><strong>Versatility:</strong> Can be molded into intricate shapes and designs with ease.</li>
                    <li><strong>Durability:</strong> Hardens into a permanent form after baking, making it suitable for long-lasting works.</li>
                    <li><strong>Color Range:</strong> Available in a variety of vibrant colors that can be mixed to create custom shades.</li>
                    <li><strong>Texture:</strong> Capable of achieving both smooth finishes and highly detailed textures.</li>
                </ul>

                <h3>Techniques:</h3>
                <ul>
                    <li><strong>Conditioning:</strong> Kneading the clay to soften it before sculpting.</li>
                    <li><strong>Texturing:</strong> Using tools to add surface details to the clay before baking.</li>
                    <li><strong>Baking:</strong> Hardening the sculpture in an oven to create a permanent piece.</li>
                    <li><strong>Painting:</strong> Applying acrylic or other paints to enhance the sculpture's details after baking.</li>
                </ul>

                <h3>Care and Preservation:</h3>
                <p>Polymer clay creations should be stored in a cool, dry place and kept away from extreme temperatures. A clear varnish can be applied to protect the surface and give it a glossy finish.</p>

