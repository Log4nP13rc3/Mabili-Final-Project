                <h1 class="page-heading">Mixed Media</h1>
                
                <h3>Introduction:</h3>
                <p>Mixed media involves combining different artistic mediums and materials in a single artwork. This approach allows artists to explore new textures, techniques, and forms by integrating elements such as paint, collage, and found objects.</p>
                
                <h3>History:</h3>
                <p>Mixed media became prominent in the 20th century as artists sought to break traditional boundaries and experiment with new methods. It has been a popular approach in contemporary art, reflecting the diversity of modern artistic practices.</p>

                <h3>Characteristics:</h3>
                <ul>
                    <li><strong>Versatility:</strong> Incorporates a variety of materials and techniques in one piece.</li>
                    <li><strong>Texture:</strong> Adds depth and tactile qualities through different materials.</li>
                    <li><strong>Innovation:</strong> Encourages experimentation and creative combinations.</li>
                    <li><strong>Complexity:</strong> Often results in multi-layered and complex artworks.</li>
                </ul>

                <h3>Techniques:</h3>
                <ul>
                    <li><strong>Collage:</strong> Incorporating cut-out materials like paper, fabric, or photographs.</li>
                    <li><strong>Assemblage:</strong> Combining three-dimensional objects with paint or other media.</li>
                    <li><strong>Layering:</strong> Building up multiple layers of different materials to create depth and interest.</li>
                    <li><strong>Textural Effects:</strong> Using materials like sand, fabric, or metal to add texture to the artwork.</li>
                </ul>

                <h3>Care and Preservation:</h3>
                <p>Mixed media artworks should be handled with care, and protective measures should be taken to preserve various materials. Store in a controlled environment to avoid deterioration of materials.</p>

