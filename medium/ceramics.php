                <h1 class="page-heading">Ceramics</h1>
                
                <h3>Introduction:</h3>
                <p>Ceramics involves the creation of objects from clay and other ceramic materials, which are then fired at high temperatures to harden. It includes a range of techniques from pottery to sculptural work and is known for its versatility and durability.</p>
                
                <h3>History:</h3>
                <p>Ceramics has a rich history dating back thousands of years. Early ceramics were functional items like pots and bowls, evolving over time into both functional and decorative pieces with sophisticated techniques and artistic expression.</p>

                <h3>Characteristics:</h3>
                <ul>
                    <li><strong>Durability:</strong> Fired ceramics are hard and durable, making them ideal for both functional and decorative purposes.</li>
                    <li><strong>Variety of Techniques:</strong> Includes wheel throwing, hand-building, and slip casting.</li>
                    <li><strong>Surface Treatments:</strong> Glazes and underglazes can be applied to create various textures and finishes.</li>
                    <li><strong>Versatility:</strong> Suitable for a wide range of forms, from everyday items to intricate sculptures.</li>
                </ul>

                <h3>Techniques:</h3>
                <ul>
                    <li><strong>Wheel Throwing:</strong> Shaping clay on a potterâ€™s wheel to create symmetrical forms.</li>
                    <li><strong>Hand Building:</strong> Creating objects by shaping clay with hands and tools, often used for more organic or sculptural forms.</li>
                    <li><strong>Slip Casting:</strong> Pouring liquid clay (slip) into molds to create hollow objects.</li>
                    <li><strong>Glazing:</strong> Applying a liquid glass coating that is fused to the surface during firing to create color and texture.</li>
                </ul>

                <h3>Care and Preservation:</h3>
                <p>Ceramic objects should be handled carefully to avoid chipping. Store in a stable environment to prevent cracking from temperature changes. Clean with a soft cloth and avoid abrasive cleaners.</p>

