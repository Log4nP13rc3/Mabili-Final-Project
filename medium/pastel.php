                <h1 class="page-heading">Pastel</h1>
                
                <h3>Introduction:</h3>
                <p>Pastels are a medium made from powdered pigments and a binder, available in soft, hard, and oil variants. They are known for their vibrant colors and velvety texture, which can be used to create both detailed and expressive artworks.</p>
                
                <h3>History:</h3>
                <p>Pastels have been used since the Renaissance, with artists like Edgar Degas and Mary Cassatt popularizing the medium in the 19th century. Their ease of use and vibrant color make them a favorite among artists.</p>

                <h3>Characteristics:</h3>
                <ul>
                    <li><strong>Vibrant Colors:</strong> Offers intense, pure colors due to high pigment concentration.</li>
                    <li><strong>Texture:</strong> Provides a soft, velvety finish that can be blended for smooth transitions.</li>
                    <li><strong>Flexibility:</strong> Can be used for detailed work or broader, expressive strokes.</li>
                    <li><strong>Layering:</strong> Allows for layering and blending of colors to achieve depth and richness.</li>
                </ul>

                <h3>Techniques:</h3>
                <ul>
                    <li><strong>Blending:</strong> Using tools or fingers to smooth out and blend pastel colors.</li>
                    <li><strong>Scumbling:</strong> Applying a layer of pastel over a rough surface to create texture.</li>
                    <li><strong>Layering:</strong> Building up layers of color to create depth and intensity.</li>
                    <li><strong>Dry Brush:</strong> Using a brush to apply or blend pastel for softer effects.</li>
                </ul>

                <h3>Care and Preservation:</h3>
                <p>Pastel artworks should be protected with a fixative to prevent smudging. Store in a cool, dry place, and use glass or acrylic framing to safeguard the artwork from dust and damage.</p>

