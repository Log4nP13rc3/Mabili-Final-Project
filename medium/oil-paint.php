                <h1 class="page-heading">Oil Paint</h1>
                
                <h3>Introduction:</h3>
                <p>Oil paint is a versatile and durable medium that has been a favorite among artists for centuries. It consists of pigments suspended in a drying oil, usually linseed oil. The slow drying time of oil paint allows artists to blend colors and create detailed textures with precision.</p>
                
                <h3>History:</h3>
                <p>Oil painting techniques were first developed in the early 15th century, with early examples seen in the work of Jan van Eyck. Over time, oil painting became the dominant medium in Western art, with masters like Leonardo da Vinci, Rembrandt, and Vincent van Gogh making significant contributions to its development.</p>

                <h3>Characteristics:</h3>
                <ul>
                    <li><strong>Slow Drying Time:</strong> Allows for extended blending and reworking of the paint.</li>
                    <li><strong>Rich Color:</strong> Oil paints offer vibrant, deep colors due to the high pigment content.</li>
                    <li><strong>Versatility:</strong> Can be applied in thin glazes or thick impasto, allowing for a range of textures and effects.</li>
                    <li><strong>Durability:</strong> When properly varnished and cared for, oil paintings can last for centuries.</li>
                </ul>

                <h3>Techniques:</h3>
                <ul>
                    <li><strong>Glazing:</strong> A technique involving the application of thin, transparent layers of paint to achieve luminosity and depth.</li>
                    <li><strong>Impasto:</strong> The application of thick, textured paint that stands out from the surface, creating a three-dimensional effect.</li>
                    <li><strong>Scumbling:</strong> A dry-brush technique used to soften or blend areas of color, adding texture.</li>
                    <li><strong>Alla Prima:</strong> A technique where the painting is completed in one sitting, with wet-on-wet paint application.</li>
                </ul>

                <h3>Care and Preservation:</h3>
                <p>Oil paintings require specific care to ensure their longevity. This includes proper varnishing, protection from direct sunlight, and maintaining stable humidity and temperature levels. Regular cleaning and professional restoration may also be necessary over time.</p>

