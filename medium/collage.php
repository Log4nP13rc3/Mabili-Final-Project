                <h1 class="page-heading">Collage</h1>
                
                <h3>Introduction:</h3>
                <p>Collage is an art technique where various materials such as paper, fabric, and other objects are assembled onto a surface to create a new, unified piece. It allows for creative combinations and textures by layering and juxtaposing elements.</p>
                
                <h3>History:</h3>
                <p>Collage originated in the early 20th century, with artists like Pablo Picasso and Georges Braque pioneering the technique as part of Cubism. It quickly became a popular method for exploring new forms and ideas in modern art.</p>

                <h3>Characteristics:</h3>
                <ul>
                    <li><strong>Mixed Media:</strong> Combines various materials to create texture and depth.</li>
                    <li><strong>Layering:</strong> Involves layering different elements to build up a composition.</li>
                    <li><strong>Variety of Materials:</strong> Can include paper, fabric, photographs, and more.</li>
                    <li><strong>Creative Freedom:</strong> Offers limitless possibilities for artistic expression and innovation.</li>
                </ul>

                <h3>Techniques:</h3>
                <ul>
                    <li><strong>Paper Collage:</strong> Assembling pieces of paper in a layered or cut-out format.</li>
                    <li><strong>Photo Collage:</strong> Combining photographs to create new visual narratives.</li>
                    <li><strong>Assemblage:</strong> Incorporating three-dimensional objects into a collage to add depth.</li>
                    <li><strong>Decoupage:</strong> Gluing paper cut-outs onto a surface and applying a protective layer.</li>
                </ul>

                <h3>Care and Preservation:</h3>
                <p>Protect collages from direct sunlight and moisture to prevent fading and deterioration. Frame under glass or store in a protective sleeve to avoid physical damage.</p>

